export function validateMPG(mpg: number): boolean {
  return mpg > 0 && isFinite(mpg);
}

export function findVehicleById(vehicles: Vehicle[], id: string): Vehicle | undefined {
  return vehicles.find(v => v.id === id);
}

export function findVehicleByRegistration(vehicles: Vehicle[], registration: string): Vehicle | undefined {
  if (!registration) return undefined;
  const regUpper = registration.toUpperCase();
  return vehicles.find(v => v.registration && v.registration.toUpperCase() === regUpper);
}

// Get MPG based on make/model
export function getMPGByMakeModel(make: string, model: string): number {
  const mpgMap: Record<string, Record<string, number>> = {
    'Fiat': { 'Ducato': 27 },
    'MAN': { 'TGE': 35 },
    'MAN (B)': { 'TGE': 35 },
    'Renault': { 'Master': 33 },
    'Ford': { 'Transit': 23 }
  };
  return mpgMap[make]?.[model] || 35; // default 35
}
